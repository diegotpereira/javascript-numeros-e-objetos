// Como o nome sugere, o método reverse 
// inverte os elementos de um vetor, 
// fazendo com que o último se torne o 
// primeiro, o primeiro se torne o último 
// e os intermediários sigam a mesma lógica. 
// A chamada a este método não requer nenhum parâmetro.
var vetor1 = new Array(1, 2, 3, 4, 5)
vetor1.reverse()
document.write(vetor1)